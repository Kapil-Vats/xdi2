package xdi2.discovery;

public class XDIResolutionException extends Exception {

	private static final long serialVersionUID = 4500240992794516180L;

	public XDIResolutionException() {

		super();
	}

	public XDIResolutionException(String message) {

		super(message);
	}

	public XDIResolutionException(Throwable cause) {

		super(cause);
	}

	public XDIResolutionException(String message, Throwable cause) {

		super(message, cause);
	}
}
